; Write an assembly language program to flash “SPICE” only
; 10 times (display SPICE for 2 sec and blank for 1 sec).

; 8050H:
; 37 60 93 97 00 00 D6

MOV R0, #0AH 				; REPEAT COUNT
ITER:
	CLR D5H 				; LOAD FROM CODE MEMORY
	MOV DPTR, #8050H 		; STARTING LOCATION OF THE LOOK-UP TABLE
	LCALL OUTPUT 			; OUTPUT UTILITY ROUTINE
	; DELAY FOR 2 SECONDS
	LCALL DELAY
	LCALL DELAY

	LCALL CLEAR 			; CLEAR UTILITY ROUTINE
	LCALL DELAY 			; DELAY FOR 1 SECOND

	DJNZ R0, ITER 			; REPEAT IN A LOOP

	LCALL 0000H 			; STOP EXECUTION

DELAY:
	; AWAIT (10H*0FAH*0FAH) MACHINE CYCLES => 1 SECOND
	MOV R3, #10H
	UP1:
		MOV R4, #0FAH
		UP2:
			MOV R5, #0FAH
		DJNZ R4, UP2
	DJNZ R3, UP1
	RET 					; RETURN BACK TO THE MAIN LOOP
	END
