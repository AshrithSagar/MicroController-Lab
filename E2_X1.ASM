; DIFFERENCE OF TWO BCD NUMBERS IN R1, R2 AND STORE THE RESULT IN 2000H

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START: 	MOV A, R1
		SUBB A, R2 		; PERFORMS (R1-R2)
		
		JNC STORE		; JUMP ON NO CARRY TO THE LABEL STORE

CNVRT:	MOV R0, A
		MOV A, #00H
		SUBB A, R0

STORE:	MOV DPTR, #2000H
		MOVX @DPTR, A
		END
