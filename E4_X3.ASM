; Implement an 8-bit octal up counter.

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	MOV R0, #00H 		; INITIALISE
	
ITER:
	MOV A, R0 			; LOAD THE COUNTER
	INC A 				; INCREMENT THE ACCUMULATOR
	
CYCLE:
	CLR C 				; CLEAR CARRY
	SUBB A, #08H 		; CHECK FOR BASE

	JNC CYCLE
