; FIND THE SUM OF TEN 16-BIT HEXADECIMAL NUMBERS AVAILABLE IN MEMORY 
; STARTING FROM LOCATION XX10H. STORE THE RESULT IN XX50H ONWARDS

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	MOV DPTR, #1010H
	MOV R1, #10

UP:
	MOV A, @DPTR
	MOV 0F0H, A
	ADDC A, B
	INC DPTR
	DJNZ R1, UP

STORE:
	MOV DPTR, #1050H
	MOV @DPTR, A

	END
