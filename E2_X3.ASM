; Multiply two 16-bit hex numbers and store the product.

; ASSUME THE NUMBERS ARE STORED IN R0, R1, R2, R3
; WILL STORE THE PRODUCT USING THE STACK POINTER.

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	MOV A, R0			; LSB_1
	MOV 0F0H, A 		; COPY IN B
	MOV A, R2 			; LSB_2
	MUL AB 				; LSB_1 TIMES LSB_2
	PUSH 0E0H 			; LSB OF CURRENT PRODUCT
	PUSH 0F0H 			; MSB OF CURRENT PRODUCT

	MOV A, R0			; LSB_1
	MOV 0F0H, A 		; COPY IN B
	MOV A, R3 			; MSB_2
	MUL AB 				; LSB_1 TIMES MSB_2
	DEC SP
	PUSH 0E0H 			; LSB OF CURRENT PRODUCT
	PUSH 0F0H 			; MSB OF CURRENT PRODUCT

	END
		