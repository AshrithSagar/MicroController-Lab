; Multiply two 16-bit hex numbers and store the product.

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	; LOW TIMES LOW
	MOV DPTR, #1000H	; GET LOW BYTE OF FIRST HEX NUMBER
	MOVX A, @DPTR 		
	MOV 0F0H, A 		; MAKE A COPY IN REG B
	MOV DPTR, #1002H	; GET LOW BYTE OF SECOND HEX NUMBER
	MOVX A, @DPTR
	MUL AB 				; MULTIPLY THE LOW BYTES OF THE TWO NUMBERS
	MOV R0, A 			; LOW BYTE OF PRODUCT IN R0
	MOV A, B
	MOV R1, A 			; HIGH BYTE OF PRODUCT IN R1

	; LOW TIMES HIGH
	MOV DPTR, #1000H	; GET HIGH BYTE OF FIRST HEX NUMBER
	MOVX A, @DPTR 		
	MOV 0F0H, A 		; MAKE A COPY IN REG B
	MOV DPTR, #1003H	; GET HIGH BYTE OF SECOND HEX NUMBER
	MOVX A, @DPTR
	MUL AB 				; MULTIPLY THE LOW BYTES OF THE TWO NUMBERS

	; HIGH TIMES LOW
	MOV DPTR, #1001H	; GET HIGH BYTE OF FIRST HEX NUMBER
	MOVX A, @DPTR 		
	MOV 0F0H, A 		; MAKE A COPY IN REG B
	MOV DPTR, #1002H	; GET HIGH BYTE OF SECOND HEX NUMBER
	MOVX A, @DPTR
	MUL AB 				; MULTIPLY THE LOW BYTES OF THE TWO NUMBERS

	; HIGH TIMES HIGH
	MOV DPTR, #1001H	; GET HIGH BYTE OF FIRST HEX NUMBER
	MOVX A, @DPTR 		
	MOV 0F0H, A 		; MAKE A COPY IN REG B
	MOV DPTR, #1003H	; GET HIGH BYTE OF SECOND HEX NUMBER
	MOVX A, @DPTR
	MUL AB 				; MULTIPLY THE LOW BYTES OF THE TWO NUMBERS
	
