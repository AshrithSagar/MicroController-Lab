; Implement an 8 bit ring counter.

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	MOV A, #01H 		; INITIALISE
	CLR C 				; CLEAR CARRY

ITER:
	RLC A 				; LEFT SHIFT ACCUMULATOR
	MOV P2, A 			; SHOW IN PORT-2
	
	MOV R0, #0FFH		; AWAIT (0FFH) MACHINE CYCLES
	DELAY:
		NOP
	DJNZ R0, DELAY

	SJMP ITER 	 		; REPEAT IN A LOOP
	END
