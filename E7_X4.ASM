; Roll “SPICE” continuously (a) left side (b) right side.

; 8050H:
; 37 60 93 97 00 00 D6

START:
	SETB PSW.5 				; LOAD FROM EXTERNAL DATA MEMORY
	MOV DPTR, #8050H 		; STARTING LOCATION OF THE LOOK-UP TABLE
	LCALL OUTPUT 			; OUTPUT UTILITY ROUTINE
	LCALL DELAY 			; AWAIT

	MOV R0, #07H 			; REPEAT FOR 7 BYTES
	MOV DPTR, #8050H 		; STARTING LOCATION OF THE LOOK-UP TABLE
MODIFY:
	
	
	DJNZ R0, MODIFY

	LJMP START 				; REPEAT IN A LOOP

DELAY:
	; AWAIT (10H*0FAH*0FAH) MACHINE CYCLES => 1 SECOND
	MOV R3, #10H
	UP1:
		MOV R4, #0FAH
		UP2:
			MOV R5, #0FAH
		DJNZ R4, UP2
	DJNZ R3, UP1
	RET 					; RETURN BACK TO THE MAIN LOOP

OUTPUT:
	RET
END
