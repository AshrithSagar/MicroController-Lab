; Roll “SPICE” continuously (a) left side (b) right side.

; 8050H:
; 37 60 93 97 00 00 D6

; 8000H:
START:
	SETB PSW.5 				; D2 D5 	; LOAD FROM EXTERNAL DATA MEMORY
	MOV DPTR, #8050H 		; 90 80 50 	; STARTING LOCATION OF THE LOOK-UP TABLE
	LCALL OUTPUT 			; 12 02 55 	; OUTPUT UTILITY ROUTINE
	LCALL DELAY 			; 12 _A0 00	; AWAIT

	MOV DPTR, #8050H 		; 90 80 50 	; STARTING LOCATION OF THE LOOK-UP TABLE
	MOVX A, @DPTR 			; GET STARTING DATA, FOR LEFT ROTATE CASE
	MOV F0H, A 				; SAVE IN REG (B)

	MOV R0, #07H 			; 78 07 	; REPEAT FOR 7 BYTES

MODIFY:
	INC DPTR 				; GO TO NEXT LOCATION
	MOVX A, @DPTR 			; GET DATA FROM NEXT LOCATION
	DEC_DPTR 				; GO TO CURRENT LOCATION
 	MOVX @DPTR, A 			; COPY DATA TO CURRENT LOCATION

 	INC DPTR 				; UPDATE TO NEXT LOCATION
	DJNZ R0, MODIFY 		; D8 _FE 	; REPEAT FOR ALL IN THE LOOK-UP TABLE

	LJMP START 				; 02 00 00 	; REPEAT IN A LOOP

DEC_DPTR:
	MOV A, 82H 				; GET (DPL)
	ADD A, #01H 			; INCREMENT (DPL)
	MOV 82H, A 				; SAVE (DPL)
	MOV A, 83H 				; GET (DPH)
	ADDC A, #00H 			; ADD CARRY TO (DPH)
	MOV 83H, A 				; SAVE (DPH)
	RET 					; RETURN BACK TO MAIN

; A000H:
DELAY:
	; AWAIT (10H*0FAH*0FAH) MACHINE CYCLES => 1 SECOND
	MOV R3, #10H 			; 7B 10
	UP1:
		MOV R4, #0FAH 		; 7C FA
		UP2:
			MOV R5, #0FAH 	; 7D FA
		DJNZ R4, UP2 		; DC FC
	DJNZ R3, UP1 			; DB F8
	RET 					; 22 		; RETURN BACK TO THE MAIN LOOP

OUTPUT:
	RET
