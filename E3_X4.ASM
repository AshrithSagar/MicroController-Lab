	MOV R0, #92H 		; INPUT 2-DIGIT HEXADECIMAL NUMBER
	
	MOV 0F0H, #100 		; DIVISION BY DECIMAL 100
	MOV A, R0 			; DUPLICATE THE NUMBER TO ACCUMLATOR
	DIV AB 				; PERFORM DIVISION

	


