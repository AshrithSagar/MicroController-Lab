; Convert a 2-digit hexadecimal number into BCD number.

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	MOV A, #92H 		; INPUT 2-DIGIT HEXADECIMAL NUMBER
	ADD A, #00H 		; ADD 00H TO BE ABLE TO USE DA A
	DA A 				; DECIMALLY ADJUST ACCUMULATOR
	
	MOV R0, A 			; LOWER BCD RESULT REGISTER
	MOV R1, #00H 		; HIGHER BCD BYTE; CLR R1 DOESN'T WORK;

	JNC HERE 			; EXIT IF HIGHER BYTE IS 00H
	
	INC R1 				; SET HIGHER BCD BYTE

HERE:
	SJMP HERE			; LOGICAL END
	END
