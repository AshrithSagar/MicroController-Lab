; Convert a 2-digit hexadecimal number into BCD number.

ORG 0000H				; ORIGINATE
AJMP START				; JUMP TO THE LABEL START

START:
	MOV R0, #92H 		; INPUT 2-DIGIT HEXADECIMAL NUMBER
	ADD A, 00H 			; ADD 0, SO AS TO USE DA A
	DA A 				; DECIMALLY ADJUST ACCUMULATOR
	

HERE:
	SJMP HERE			; LOGICAL END
	END
